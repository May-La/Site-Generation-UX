<body>
    <br>
    <h1 id = "titre">Protocole de test</h1>
    <br>
    <h2>Introduction</h2>
    <br>
    <p>Nous sommes quatre élèves de l'ENSC et dans le cadre de notre projet transdisciplinaire Génération UX nous cherchons à adapter l'échelle MeCue a des enfants de primaire. L’échelle MeCue est une échelle UX et sert donc à tester la qualité globale de l’interaction entre un utilisateur et un système. Celle-ci en particulier évalue la perception du système, les émotions provoquées, les conséquences de l’usage et l’évaluation globale. Cette échelle existe et est adaptée pour un adulte, et notre objectif est de tester une adaptation de cette échelle pour qu’elle convienne à des enfants entre 6 et 10 ans.</p>
    <p>Cette adaptation intervient dans le cadre de la thèse de notre cliente Floriane Balssa, encadrée par Véronique Lespinet-Najib, qui s’est rendue compte que la plupart des échelles n’étaient pas adaptées aux enfants.</p>
    <br>
    <hr ALIGN=CENTER WIDTH="50%"> 
    <br>
    <h2>Objectif de l'exercice</h2>
    <br>
    <p>Afin de confirmer si notre adaptation a été utile nous allons tester une échelle : MeCue adaptée sur les enfants</p>
    <br>
    <hr ALIGN=CENTER WIDTH="50%"> 
    <br>
    <h2>Plan d’action</h2>
    <br>
    <p>
        Expliquer objectifs de notre venue brièvement et déroulement de l’expérience :
        <br> 
        “Bonjour! on est un groupe de 4 étudiants et pour nos études nous avons adapté un questionnaire pour des enfants de votre âge . Maintenant on a besoin de tester ce questionnaire et c’est pour ça qu’on est là . On va vous faire faire  un labyrinthe et après vous devrez répondre à un petit questionnaire avec des questions sur le labyrinthe . vous serez par groupe de 4 et vous passerez les uns après les autres ”
    </p>
    <p>“N'hésitez pas si vous avez des questions ou des remarques à n’importe quel moment, ces évaluations et les résultats resteront complètement anonymes.”</p>
    <p>La passation se fera par groupes de 4 enfants que l’on va isoler du reste de la classe pour leur faire passer le test. Le lieu exact sera à déterminer sur place en fonction de la place disponible. Si une salle annexe est disponible, on la privilégiera, sinon nous aménagerons un coin de classe pour la passation du test, et nous irons dans un autre endroit (dans la cour d’école ou autre) pour le focus group. Avec cet aménagement, les enfants seront en capacité de se concentrer mais ne se sentiront pas isolés.</p>
    <p>Expliquer l’objectif de l’exercice :</p>
    <ul>
        <li>Anonymat et préciser que l’exercice n’est pas noté, donc il n’y a pas de pression à avoir.</li>
        <li>Cette activité se déroule en deux parties. Nous allons commencer par un petit jeu individuel et ensuite on fera un petit test à propos du ressenti sur le jeu. </li>
        <li>N'hésitez pas si vous avez des questions ou des remarques à n’importe quel moment (les encourager à participer, même si ça n’est pas devant tout le monde un encadrant peut venir leur répondre en “privé”).</li>
        <li>Nous allons former des petits groupes de 4  enfants pour organiser un focus groupe. On divisera la classe de manière à ce que les groupes soient davantage gérables et que les enfants aient tous la possibilité de s’exprimer afin de récupérer plus d’avis, tout en évitant d’isoler les enfants pour qu’ils puissent être à l’aise.</li>
    </ul>
    <br>
    <hr ALIGN=CENTER WIDTH="20%"> 
    <br>
    <p><b>Première partie</b></p>
    <p>Expliquer le fonctionnement du jeu, on présentera soit : </p>
    <ul>
        <li>un labyrinthe facile et esthétique</li>
        <li>un labyrinthe facile mais sans attraits particulier</li>
        <li>un labyrinthe difficile</li>
    </ul>
    <p>
        Nous pourrons expliquer que le but sera de tracer le chemin au crayon entre l’entrée et la sortie du labyrinthe sans traverser les murs.
        Comme chaque groupe de 4 enfants sera accompagné de 2 étudiants, chacun de nous observera 2 enfants.Nous noterons sur notre grille d’observation le temps pris par l’enfant pour finir le labyrinthe, le type de labyrinthe, ses remarques si il en fait et toutes observations que nous trouverons utiles.
    </p>
    <br>
    <hr ALIGN=CENTER WIDTH="20%"> 
    <br>
    <p><b>Deuxième partie</b></p>
    <p>Expliquer le fonctionnement de l’échelle MeCue :</p>
    <p>
        “Nous allons vous faire passer un petit questionnaire pour évaluer votre ressenti sur le labyrinthe que vous venez de remplir. C’est pour savoir si le labyrinthe vous a plu et voir si cela a été compliqué pour vous ou non. quand vous voyez le mot “produit” c’est du labyrinthe qu’on parle”.
        On passera ensuite l’ échelle imprimée au préalable et on laissera les enfants la remplir. 
        Le mot “questionnaire” est à privilégier car le mot échelle est moins commun dans ce contexte pour les enfants de primaire.
        On passera ensuite l’ échelle imprimée au préalable et on laissera les enfants la remplir. 
        Nous noterons sur notre grille d’observation le temps pris par l’enfant pour finir l’échelle, ses remarques si il en fait et toutes observations que nous trouverons utiles. 
    </p>
    <br>
    <hr ALIGN=CENTER WIDTH="20%"> 
    <br>
    <p><b>Debrief de fin</b></p>
    <p>
        Le but sera de recueillir des ressentis et/ou des incompréhensions pour possiblement améliorer notre échelle à  l’aide de ces retours.
        Tant que les résultats obtenus après passage des tests ne sont pas concluants   , il nous faudrait effectuer des modifications sur l'échelle et retourner la tester avec des enfants.
    </p>
    <p>Questions du debrief de fin :</p>
    <ul>
        <li>Sur le labyrinthe :</li>
        <ul>
            <li>4-5 post-its par enfants : écrivez les mots qui décrivent le labyrinthe  (joli, amusant, long, difficile,...)</li>
            <li>Débrief</li>
            <ul>
                <li>On trie les post it: “ Dans quelle catégorie mettriez-vous les mots ?”</li>
                <li>explication des post-it: “je vois que quelqu’un a écrit …. peux-tu m’expliquer pourquoi ?”</li>
            </ul>
            <li>après debrief : avez-vous des choses à ajouter ?</li>
        </ul>
        <br>
        <li>Sur l’échelle :</li>
        <ul>
            <li>4-5 post-its par enfants : écrivez les mots qui décrivent le labyrinthe  (joli, amusant, long, difficile,...)</li>
            <li>Débrief</li>
            <ul>
                <li>On trie les post it: “ Dans quelle catégorie mettriez-vous les mots ?”</li>
                <li>explication des post-it: “je vois que quelqu’un a écrit …. peux-tu m’expliquer pourquoi ?”</li>
            </ul>
            <li>après debrief : avez-vous des choses à ajouter ?</li>
        </ul>
    </ul>
    <p>Nous prendrons des photos des post-it regroupés par les enfants afin de pouvoir revenir dessus si besoin.</p>
    <br>
    <hr ALIGN=CENTER WIDTH="50%"> 
    <br>
    <h2>Analyse à faire après la passation</h2>
    <br>
    <p>On voudra s’assurer que les labyrinthes les moins attrayants obtiennent un score moins élevé avec l’échelle adaptée afin de s’assurer de la cohérence globale de l’échelle (même si cela ne constitue pas en soi une preuve de fiabilité de l’échelle). Pour avoir une preuve de fiabilité, on utilisera également le coefficient alpha de Cronbach pour vérifier la cohérence entre les réponses aux questions d’un même module (traitant de sujets semblables). Le score obtenu par le coefficient devra être au moins de 0,7.</p>
