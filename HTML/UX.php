    <h2 id = "$2">Exemples d'échelles UX</h2> 
    <br>
    <h3 id = "$2.1">I. AttrakDiff</h3>
    <br>
    <p>
        AttrakDiff est une échelle mise au point en 2003 par Hassenzahl, Burmester et Koller, des chercheurs allemands. 
        Elle a été traduite en français en 2014 par C. Lallemand <a href = "https://lagrandeourse.design/wp-content/uploads/2021/07/Evaluation-questionnaire-Attrakdiff-Activites-UX.pdf">(Echelle complète ici)</a>. 
        Très utilisée encore aujourd’hui, elle permet d’évaluer la qualité de l’expérience utilisateur avec un système interactif en 4 critères de 7 items. 
        Ces critères sont la qualité pragmatique, la qualité hédonique-simulation, la qualité hédonique-identité et l’attractivité globale. 
    </p>
    <p>
        La qualité pragmatique correspond à l’utilisabilité du système et indique à quel point il permet aux utilisateurs d’atteindre leurs buts (dans le sens de réaliser une tâche). 
        La qualité hédonique-simulation permet de mesurer la stimulation sensorielle et intellectuelle entraînée par le système. 
        La qualité hédonique-identité évalue dans quelle mesure le système permet à l’utilisateur de s’identifier à lui.
        Enfin, l’attractivité globale décrit la valeur globale du système à partir des qualités pragmatiques et hédoniques.
    </p>
    <p>
        Chaque item est composé de deux antonymes séparés par 7 cercles, représentant l’accord ou le désaccord de l’utilisateur avec les qualifications proposées. 
        L’utilisateur cochera alors le cercle qu’il jugera le plus représentatif de son avis. 
        Les antonymes peuvent être des adjectifs ou des verbes conjugués à la troisième personne du singulier au présent comme nous pouvons le voir dans l’extrait ci-dessous <a href = "#Fig4">(Figure 4)</a>.
    </p>
    <div class = "text-center">
        <a href = "https://carinelallemand.files.wordpress.com/2015/09/version-franc3a7aise-attrakdiff_lallemand_2015.pdf"><img src = "../IMG/ExtraitAttrakDiff.png" id = "Fig4" alt = "Module 1 AttrakDiff" WIDTH="40%"></a>
        <br>
        <br>
        <h5>Figure 4. Extrait de l'échelle AttrakDiff</h5>
    </div>
    <p>
        De manière générale, l’échelle est très complète et permet de recueillir un grand nombre de données intéressantes par rapport à l’utilisateur. 
        Cependant, pour des enfants de 7 à 14 ans, le vocabulaire peut être complexe voire difficilement accessible et l’échelle est longue à compléter. 
        Pour pallier au deuxième point, une version courte du questionnaire existe, composée de dix items au vocabulaire plus accessible <a href = "#Fig5">(Figure 5)</a>. 
        Cette version a été mise au point en 2019 par Antonio Giardi dans un article scientifique intitulé “Evaluate children's User experience with AttrakDiff method: USiena”. 
        Néanmoins, elle n’existe pas en français.
    </p>
    <div class = "text-center">
        <a href = "https://shorturl.at/bsvE2"><img src = "../IMG/AttrakDiffCourt.png" id = "Fig5" alt = "AttrakDiff raccourci" WIDTH="40%"></a>
        <br>
        <br>
        <h5>Figure 5. AttrakDiff version courte</h5>
    </div>
    <p>
        Également, pour faciliter la compréhension de cette tranche d’âge, le chercheur italien Antonio Giardi a mis au point une aide visuelle en 2019 <a href = "#Fig6">(Figure 6)</a>. 
        De la même manière, cette aide n’est disponible qu’en anglais.
    </p>
    <div class = "text-center">
        <a href = "https://www.researchgate.net/figure/Questionnaire-AttrakDiff-shortened-modified-University-of-Siena_fig1_340950602"><img src = "../IMG/SmileysAttrakDiff.png" id = "Fig6" alt = "Aide à la compréhension d'AttrakDiff" WIDTH="40%"></a>
