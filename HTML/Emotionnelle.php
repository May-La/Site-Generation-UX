    <h2></h2>
    <br>
    <p>L’évaluation émotionnelle par rapport à un site ou autre est utile pour maîtriser l’impact émotionnel qu’a un système sur ses utilisateurs. Tous les systèmes qu’on rencontre au quotidien nous procurent des émotions de plus ou moins forte intensité et lorsqu’elle est positive (pour un site internet par exemple), elle va nous donner envie de réitérer l’expérience (de retourner sur le site par exemple).</p>
    <p>Le test utilisateur émotionnel, c’est-à-dire l’échelle émotionnelle, consiste à demander à l’utilisateur d’exprimer les émotions ressentis pendant l’utilisation avec des outils verbaux ou non. Dans cette partie, nous allons rassembler des informations sur les émotions, et détailler le fonctionnement de certaines échelles émotionnelles existantes.</p>
    <br>
    <hr ALIGN=CENTER WIDTH="50%"> 
    <br>
    <h2 id = "$2">Les émotions</h2>
    <br>
    <p>Selon Ekman (1992), il y a six émotions : “quatre négatives : la peur, la colère, le dégoût et la tristesse et deux positives : la joie et la surprise (ou l’intérêt)”.</p>
    <p>Les autres émotions en sont dérivées. On peut les retrouver sur la roue des émotions de Genève créée par Scherer, Shuman, Fontaine & Soriano, 2013 une échelle émotionnelle que l’on détaillera ultérieurement.</p>
    <p>Les émotions impliquent un facteur causal (souvenir, épisode affectif…) et donc des circuits cognitifs, psychophysiologiques, motivationnels et moteurs. Le système émotionnel est différent de l’humeur et de l’affect.</p>
    <p>Pour évaluer les émotions on a 3 types de composantes à prendre en compte : </p>
    <ul>
        <li><b>Les composantes physiologiques :</b> elles vont activer le système endocrinien et le système nerveux autonome.</li>
        <li><b>Les composantes subjectives :</b> ce que le sujet pense (ce dont il a conscience)</li>
        <li><b>Les composantes expressives ou comportementales :</b> expressions faciales, réflexions etc.</li>
    </ul>
    <p>Arrêtons nous plus en détail sur ces composantes.</p>
    <p><b>Les composantes physiologiques</b></p>
    <p>Ce sont les composantes qui peuvent être influencées par les émotions et dont la variation est mesurable. En voici quelques exemples : </p>
    <ul>
        <li>La fréquence cardiaque </li>
        <li>La réponse/conductance électrodermale</li>
        <li>La fréquence respiratoire</li>
        <li>L’électroencéphalographie</li>
    </ul>
    <br>
    <p><b>Les composantes comportementales ou l’expression</b></p>
    <p>Deux techniques existent pour analyser les expressions faciales :</p>
    <ul>
        <li>
            Le Facial Action Coding System (FACS), Ekman & Friesen (1978), qui permet de coder les expressions faciales en fonction de 
            la contraction de certains muscles (par exemple le sourire se caractérise par la remontée des joues et l’étirements des lèvres, on en déduit que l’individu ressent de la joie). 
            Il existe un logiciel permettant d’utiliser l’analyse comportementale car elle a intégré le FACS. Grâce à une caméra sur un PC, un mobile, ou encore une tablette, ces composantes peuvent être analysées.
            <a href = "https://www.noldus.com/facereader">https://www.noldus.com/facereader</a>
        </li>
        <li>
            L'Électromyographie  est une technique médicale permettant d’étudier la fonction des nerfs et des muscles du système périphérique. 
            On peut utiliser cette technique dans le cadre de l’étude des émotions car elle consiste ici à mesurer la tension des muscles du visage.
        </li>
    </ul>
    <p><b>Les composantes subjectives</b></p>
    <p>A l’aide d’auto-évaluation, des instruments de mesure ont été conçus pour évaluer plusieurs facteurs :</p>
    <ul>
        <li>La dimension émotionnelle : elle est déclenchée par les fonctions indépendantes du plaisir, de l’activation et de la dominance (ou du contrôle). Ces caractéristiques seront détaillées plus loin.</li>
        <li>Les émotions discrètes : on propose des labels, des adjectifs, des images ou encore des phrases et le sujet évalue à l’aide d’échelle ses émotions.</li>
    </ul>
    <p>Dans le cas des échelles utilisant des outils verbaux, le sujet choisit les mots correspondant le mieux à ses impressions, si les outils sont non verbaux, il sélectionne des émoticônes ou encore des personnages par exemple qui correspondent à ses émotions.</p>
    <p>
        Plusieurs échelles émotionnelles sont connues comme la DES (Izard et al., 1993), le questionnaire PANAS (Watson, Clark & Tellegen, 1988), l’échelle PAD (Mehrabian, 1998), l’échelle SAM (Bradley & Lang, 1994)., 
        l’échelle PrEmo (Desmet, Hekkert & Jacobs, 2000) ou encore la roue des émotions de Genève (Plutchik, 1980). Nous allons en détailler plusieurs ultérieurement.
    </p>
    <br>
    <hr ALIGN=CENTER WIDTH="50%"> 
    <br>
    <h2 id = "$3">Les émotions chez l'enfant</h2>
    <br>
    <p>Intéressons-nous à présent à la façon dont l’enfant interprète ses émotions. En effet, l’adulte et l’enfant n’ont pas la même façon de définir leurs émotions, il est donc primordial afin d’adapter une échelle émotionnelle, de comprendre comment l’enfant définit ses émotions.</p>
    <p>
        Le développement de la reconnaissance des émotions a lieu dès la petite enfance. Comme l’explique Gosselin dans <u>Le décodage de l'expression faciale des émotions au cours de l'enfance</u> (2005), un nouveau-né saura discerner différentes expressions faciales mais ce n’est qu’à la fin de la première année qu’il pourra y associer des émotions rudimentaires (joie, colère, tristesse). 
        Jusqu’à sa troisième année, son champ d’émotions s’élargit (dégoût, surprise, peur) mais il ne sait pas discerner les émotions sincères de celles qui sont forcées. C’est à partir de l’âge où l’enfant est scolarisé qu’il va savoir faire la différence.
    </p>
    <p>
        Or même si les subtilités des émotions prennent du temps, les émotions restent la première source de communication des jeunes enfants avec leur entourage. Des analyses (Wellman, Harris, Banerjee, & Sinclair, 1995) montrent que dès l’âge de 2 ans, l’enfant sait associer certains mots avec des émotions comme la joie, la tristesse, la colère ou encore la peur. Il peut également associer une expression faciale avec une émotion (Russell & Bullock, 1985). A l’âge de 6 ans, cette aptitude s’est développée pour associer les émotions de la joie, de la colère ou de la tristesse à une expression faciale comme le ferait un adulte (Gosselin, 2005).
    </p>
    <p>
        D’après le mémoire de Denis intitulé <u>L’impact des émotions sur l’attention chez l’enfant</u>, selon Saarni (2011), les élèves de l’école élémentaire sont capables de rapporter eux-mêmes des émotions et d’utiliser des mots pour expliquer des situations en lien avec les émotions. 
        Il y a un certain consensus à admettre que l’enfant aurait acquis, dès la période préscolaire, l’essentiel de son répertoire émotif (avant six/sept ans). Cependant la diversité et la complexité des événements auxquels l’enfant peut réagir vont continuer à évoluer et faire émerger des émotions plus rares. 
        D’après Simoës-Perlant & Lemercier (2018), un enfant peut facilement distinguer et évaluer deux émotions dont la valence est différente (joie/colère), mais présente des difficultés à analyser des émotions d’intensité différente chez lui (joie/satisfaction). 
    </p>
    <p>Ainsi, lors de notre adaptation d’échelle, il faudra être vigilant à bien accentuer la valence des émotions, et de moins se préoccuper de leur intensité.</p>
    <br>
    <hr ALIGN=CENTER WIDTH="50%"> 
    <br>
    <h2 id = "$4">Utilisation des échelles émotionnelles</h2>
    <br>
    <p>
        Les échelles émotionnelles sont utilisées pour évaluer des produits commerciaux, évaluer la concurrence entre 2 produits, trouver des solutions de conception ou des storyboards, ou pour évaluer le produit final. 
        L’évaluation émotionnelle est indispensable pour comprendre et optimiser l’expérience utilisateur. L'individu évalue la pertinence, l’implication et les conséquences par rapport aux buts de l’individu, le potentiel à faire face à ces conséquences et son sens par rapport à ses valeurs ou normes sociales.
    </p>
    <p>
        L’utilisation d’outils non verbaux est intéressante pour évaluer un produit à l’international. 
        En effet, la différence de langage est un frein à la globalisation de l’échelle et la différence de culture (symbolisme, coutumes) peut l’être aussi. 
        Or si les outils utilisés dans l’échelle sont non verbaux, l’évaluation émotionnelle est possible en passant par la même échelle pour toute nationalité.
    </p>
    <p>
        Cependant, ces échelles comportent souvent beaucoup d’émotions négatives (par exemple 10 questions sur 12 sont tournées négativement dans le questionnaire PANAS créé par Watson, Clark & Tellegen en 1988). 
        C’est un avantage pour les évaluations psycho clinique (pour la détection de la dépression par exemple) mais pas pour évaluer un design UX. 
        Il faut donc penser à bien équilibrer son échelle avec finesse dans ce cas là.
    </p>
    <p>
        Un autre point faible de ces échelles est qu’elles évaluent l’effet positif et négatif du produit ou de l’IHM mais ne permettent pas de comprendre pourquoi (Lallemand & Gronier, 2015). 
        En effet, les questions ne sont pas ciblées sur un aspect du produit en particulier mais sur le ressenti sur l’ensemble, ce qui peut être gênant lorsque l’on cherche à améliorer le système.
    </p>
    <p>Il est intéressant de noter qu’il existe des tests basés sur le toucher, comme le Sensual Evaluation Instrument (SEI, Isbister et al., 2007), qui consistent en la prise en main d’objets lisses, pointus ou rugueux par exemple. Ils jouent sur les dimensions émotionnelles classiques de valence, d’activation et de contrôle. Expliquons un peu mieux ces termes :</p>
    <ul>
        <li>La valence correspond à l’aspect qualitatif de l’émotion (positive versus négative ou plaisante versus déplaisante).</li>
        <li>L’activation correspond à l’intensité de l’émotion.</li>
        <li>La dominance correspond au sentiment de contrôle ressenti sur le stimulus.</li>
    </ul>
    <p>Maintenant que nous en savons davantage sur les émotions, et la façon dont elles peuvent être évaluées à travers des caractéristiques précises, nous pouvons nous concentrer sur quelques exemples d’échelles émotionnelles.</p>
    <br>
    <hr ALIGN=CENTER WIDTH="50%"> 
    <br>
    <h2 id = "$4">Exemples d'échelles</h2>
    <br>
    <h3 id = "$4.1">I. SAM (The Select Assessment for Manufacturing Test) (Bradley & Lang, 1994)</h3>
    <br>
    <div class = "text-center">
        <img src = "../IMG/ExtraitUEQ.png" id = "Fig1" alt = "Extrait d’un test SAM" WIDTH="50%">
        <br>
        <br>
        <h5>Figure 1. Extrait d’un test SAM</h5>
    </div>
    <p>Cette échelle ci-dessus en figure 1 est très intéressante car elle permet d’évaluer les trois dimensions émotionnelles citées plus haut. En effet, la valence émotionnelle est déterminée par une extrémité ou l’autre de la ligne du sentiment représenté par la figure, l’activation est déterminée par la largeur de l’échelle, et la dominance, c’est-à-dire le contrôle de soi-même est représentée par la deuxième ligne ci-dessus. <a href = "">(Échelle complète ici)</a> </p>
    <p>Une étude a été faite pour évaluer la fiabilité et la validité de cette échelle afin d’évaluer l’activité ambulatoire (marche) d’enfants valides (McDonald, Widman, Abresch, Walsh, S. A., & Walsh, D. D. , 2005). Selon les auteurs, cette échelle est efficace, sur les tranches d’âges de 6 à 20 ans, pour contrôler leur activité, et est simple d’utilisation avec un respect des consignes accru.</p>
    <hr ALIGN=CENTER WIDTH="20%"> 
    <br>
    <h3 id = "$4.2">II. PANAS (The Positive and Negative Affect Schedule) (Watson, Clark & Tellegen, 1988)</h3>
    <br>
    <p>
        Cette échelle, que l’on retrouve ci-dessous en figure 2, gère surtout la dimension émotionnelle de valence et d’activation avec un curseur <a href = "">(échelle complète ici)</a>.
    </p>
    <div class = "text-center">
        <img src = "../IMG/ExtraitUEQ.png" id = "Fig2" alt = "Test PANAS" WIDTH="50%">
        <br>
        <br>
        <h5>Figure 2. Test PANAS</h5>
    </div>
    <p>
        Ce test a été utilisé avec des enfants pour diagnostiquer des sujets souffrant d’anxiété voire de dépression. En effet, le test correspondant est le PANAS-C (The Positive and Negative Affect Schedule for Children). 
        Il est plus court que le PANAS. Il est composé de 27 items contre 60 dans la version étendue du PANAS (seulement 20 items dans sa version réduite), avec 15 émotions positives et 12 émotions négatives. Cette échelle présente une structure binaire, où les émotions positives et négatives sont peu ou pas corrélées. 
        Il s’avère que dans ce test, la distinction entre les enfants souffrant d’anxiété et ceux souffrant de dépression n’était pas très claire. Or le test original (PANAS), qui présente une structure ternaire, s'est avéré efficace chez les enfants et les adolescents également selon l’étude de Ortuño-Sierra,  Bañuelos, Albéniz, de, Molina & Fonseca-Pedrero, (2019), et permet en plus de distinguer les deux. 
        Malgré cette étude, peu d’autres ont été menées pour valider son efficacité chez les enfants, la fiabilité de ce test pour les différentes tranches d’âges reste donc encore à déterminer. 
    </p>
    <hr ALIGN=CENTER WIDTH="20%"> 
    <br>
    <h3 id = "$4.3">III. PrEmo (Desmet, Hekkert & Jacobs, 2000)</h3>
    <br>
    <p>
        Cette échelle se présente sous la forme de douze émotions représentées par des personnages animés (animation visuelle et sonore d’une durée d’environ 1 seconde) <a href = "">(Échelle complète ici)</a>. 
        Cela est une idée intéressante pour captiver l’enfant car les animations sont efficaces et ludiques de faire passer un test.
    </p>
    <p>La passation se profile comme décrit ci-dessous : </p>
    <ul>
        <li>Une question sur l’humeur générale est posée au moment de la prise du test.</li>
        <li>Une explication de la façon d’évaluer ses émotions est donnée à l’aide de personnages.</li>
        <li>Un exercice d’évocation des émotions par rapport à une image d’un produit quelconque est testé.</li>
        <li>L’évaluation par rapport à un système est faite.</li>
    </ul>
    <p>L’évaluation par rapport à un système se fait grâce à des questions telles que « À quel point les sentiments exprimés par les personnages correspondent-ils à vos propres sentiments envers le produit ? Cliquez sur chaque personnage et utilisez les échelles pour évaluer si les sentiments exprimés par le personnage correspondent à vos propres sentiments envers le produit montré sur l’image. Vous ne pourrez passer à la page suivante qu’après avoir évalué vos sentiments via les 12 personnages. ».</p>
    <p>Comme illustré en figure 3, pour chaque personnage, le participant doit se positionner sur une échelle allant de 0 (je ne ressens pas cela) à 4 (je ressens fortement cela).</p>
    <div class = "text-center">
        <img src = "../IMG/ExtraitUEQ.png" id = "Fig3" alt = "Extrait de l’échelle PrEmo" WIDTH="50%">
        <br>
        <br>
        <h5>Figure 3. Extrait de l’échelle PrEmo</h5>
    </div>
    <p>Ce test permet donc d’évaluer la valence des émotions, et son activation mais pas sa dominance.</p>
    <p>
        Une étude faite par Schouteten, Verwaeren, Lagast, Gellynck & De Steur,(2018), avait pour but d’évaluer le profil émotionnel d’un consommateur d’un aliment. 
        Elle a montré que, pour un enfant, l'évaluation des émotions par l’utilisation d’émojis permettait de mieux prévoir ce que l’enfant aimait, ou mangeait, par rapport à l’évaluation avec des outils verbaux. 
        Or, même si cette échelle a été utilisée dans des évaluations sur de la nourriture, aucune étude ne présente des résultats où cette échelle a été testée avec des enfants.
    </p>
    <hr ALIGN=CENTER WIDTH="20%"> 
    <br>
    <h3 id = "$4.4">IV. Le test de la roue des émotions de Genève (Scherer, 2005 ; Scherer, Shuman, Fontaine & Soriano, 2013)</h3>
    <br>
    <p>
        L’échelle émotionnelle suivante est la roue des émotions de Genève, visible en figure 4 ci-dessous. Par rapport aux tests vus précédemment, celui-ci a quelques avantages. 
        Il est possible de dire que le sujet n’a absolument pas ressenti une émotion. De plus,  une case “Autre” est disponible, donc l’expression d’une émotion auquelle le concepteur de l’échelle n’a pas pensé est possible. 
        Par contre, un autre avantage est que les 3 dimensions émotionnelles sont prises en compte avec les hémisphères droite-gauche de la roue qui représentent la valence, les hémisphères nord-sud qui représentent la dominance et les échelles pour chaque émotion qui permettent l’activation.
    </p>
    <div class = "text-center">
        <img src = "../IMG/ExtraitUEQ.png" id = "Fig4" alt = "La roue des émotions de Genève" WIDTH="50%">
        <br>
        <br>
        <h5>Figure 4. La roue des émotions de Genève</h5>
    </div>
    <p>La passation du test peut se faire de 3 façons différentes :</p>
    <ul>
        <li>Le participant doit choisir et évaluer une seule émotion.</li>
        <li>Le participant peut choisir et évaluer plusieurs émotions.</li>
        <li>Le participant doit évaluer toutes les émotions de la roue.</li>
    </ul>
    <p>
        Le test commence par un paragraphe assez long pour expliquer le fonctionnement et si jamais on évalue plusieurs émotions, le paragraphe suivant est mentionné : « Nos réactions
        émotionnelles sont parfois complexes ou mixtes et peuvent englober beaucoup de composantes différentes. Veuillez cocher dans la roue l’intensité des différentes émotions ayant contribué à ce que vous avez ressenti, 
        même si les intensités de ces émotions sont différentes. Si vous n’avez ressenti aucune émotion, veuillez cocher le demi-cercle du haut au centre de la roue (Aucune). Si vous avez fait l’expérience d’une émotion très différente de celles proposées dans la roue, 
        veuillez cocher le demi-cercle du bas (Autre). »
    </p>
    <p>
        Les instructions complètes en français sont disponibles gratuitement en ligne, sur le site : 
        <a href = "www.affective-sciences.org/gew">www.affective-sciences.org/gew</a>
    </p>
    <p>
        Cette technique d’évaluation des émotions a été utilisée pour diverses raisons. 
        Par exemple, dans l’étude menée par Moysan, Torres,  & Augustin, B.-M. lors du 11ème congrès de l’Académie de l’Entrepreneuriat et de l’Innovation en 2019, on voulait étudier les émotions ressenties par les entrepreneurs de PME lorsqu’ils étaient en situation de surcharge de travail (la volontarité de cette surcharge de travail n’a pas été prise en compte) ou en situation de procédure collective (procédure organisant le règlement des dettes et la liquidation éventuelle des biens d'une entreprise en difficulté, par exemple un redressement ou une liquidation judiciaire). 
        L’utilisation de la roue des émotions de Genève permet de classer les émotions ressenties précisément selon la positivité ou la négativité de l’émotion et la puissance de l’émotion, et de comparer les émotions ressenties en situation de surcharge de travail ou de procédure collective. Une conclusion de cette expérience a été que les émotions majeures ressenties lors d’une procédure collective sont “la déception, la colère, la tristesse, et le regret”. 
        Le spectre émotionnel associé est assez complet.
    </p>
    <p>
        Mais cette technique a également pu être utilisée dans d’autres contextes, par exemple lors d’associations d’émotions avec les couleurs (Althaus, 2016). 
        L’important est de comprendre que la roue des émotions de Genève est utilisée pour aboutir à des spectres émotionnels assez complexes et détaillés (différenciation entre la sensation de paix et de nostalgie par exemple). 
        Une étude menée par Zhang, Markopoulos & Bekker (2018) utilise la roue des émotions de Genève pour évaluer l’impact émotionnel du DBL (Design-Based Learning) sur l’apprentissage chez des enfants de 12-13 ans. Pour des enfants plus jeunes, cette technique n’est pas utilisée.
    </p>
