    <h2 id = "$5">Synthèse</h2>
    <br>
    <p>Les échelles émotionnelles sont nombreuses. On a déjà des exemples comme PrEmo, la roue des émotions de Genève, Panas ou encore SAM. Ces tests permettent d’analyser les émotions subjectives via des outils verbaux ou non-verbaux auquel le sujet peut s’identifier.</p>
    <p><b>Fonctionnement :</b> La passation se fait généralement par une question sur l’humeur du participant, puis vient une explication du fonctionnement du test. Parfois une phase de test est créée permettant d’évaluer les émotions sur un sujet quelconque, puis l’évaluation des ressentis par rapport au produit en général est faite (pas de questions précises sur des points particuliers du système, plutôt sur l’ambiance générale).</p>
    <p><b>Problèmes :</b> Les résultats de l’échelle n’aboutissent pas forcément à des idées pour l’amélioration du système car le ressenti est général et les émotions sont subjectives. Nous n’avons donc pas de mesures sur des points précis, et les réponses peuvent être biaisées par des influences extérieures conscientes ou inconscientes.</p>
    <p><b>Pour notre projet :</b>  on peut s’inspirer des échelles émotionnelles car elles sont ludiques, imagées, pas très longues et permettent d’évaluer l’impact émotionnel d’un système sur un enfant. Or elles présentent des limites via la complexité des émotions qu’un enfant peut ressentir et reconnaître, et l’imprécision des données qu’elles nous procurent. </p>

