<H2>Contexte</H2>

<p>
Dans le cadre de sa thèse, la doctorante Floriane Balsa, notre cliente, s’est rendue compte que les échelles standardisées n’étaient pas adaptées pour les enfants de moins de 14 ans. Notre projet s'appelle “Génération UX”. Il vise à pallier ce manque en questionnant l'usage de ces échelles par les enfants et en les adaptant afin d’estimer le ressenti d'enfants à propos d’un système quelconque. 
</p>
<p>
Notre projet prend comme donnée d’entrée une échelle d’évaluation existante : l'échelle meCue.Nous avons produits à terme une modification de cette échelle plus apte à être utilisée auprès d’enfants.
</p>



