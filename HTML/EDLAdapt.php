    <h2>Échelles d’évaluation de la douleur</h2>
    <br>
    <p>
        Il existe plusieurs échelles pour évaluer la douleur chez l’enfant, elles sont surtout utilisées dans le cadre médical.
        On distingue deux types d’échelles distincts :
    </p>
    <p>
        Les échelles d’hétéro-évaluations dont le principe consiste en une observation par un tiers de la douleur ressentie par l’enfant à l’aide des réactions et des expressions de ce dernier. 
        Ces échelles sont souvent privilégiées chez les enfants en bas âge pour qui l’auto-évaluation est délicate voir impossible. <br>
        Dans cette catégorie il existe beaucoup d’échelles dont :
    </p>
    <ul>
        <li>
            l'échelle CHEOPS (Children’s Hospital of Eastern Ontario Pain Scale) (Figure 19) développée pour les enfants entre 1 et 5 ans, il s'agit de l’une des échelles les plus utilisées. L’observateur, qui le plus souvent est un membre du service hospitalier mais qui peut également être un parent, se charge de remplir la grille en fonction des caractéristiques observables sur l’enfant. Cela mène à l’obtention d’un score compris entre 4 et 13 censé quantifier la douleur ressentie par l’enfant.
        </li>
        <img src = "#" alt = "" id = "Fig1">
        <h5>Figure 1. Echelle CHEOPS</h5>
        <br>
        <li>
            l’échelle PIPP (Premature Infant Pain Profile) (Figure 20) pour évaluer la douleur aiguë chez le nouveau-né prématuré (mais également si le nouveau-né est à terme). Comme pour celle présentée précédemment, cette échelle fonctionne grâce à un système de points, avec un score allant de 0 à 21 obtenu à la suite du remplissage de la grille présentée ci-dessous.
        </li>
        <img src = "#" alt = "" id = "Fig2">
        <h5>Figure 2. Echelle PIPP</h5>
        <br>
        <li>Il existe encore de nombreuses autres échelles d’hétéro-évaluation (comme par exemple les échelles EVENDOL, EDIN, OPS, DEGR, QDSA, DAN, FLACC…) mais nous n’allons pas détailler chacune d’entre elles.</li>
    </ul>
    <p>
        Les échelles d’auto-évaluation dont le principe consiste en une estimation par le sujet (ici un enfant) de la douleur qu’il ressent. 
        Pour pouvoir utiliser ce type d’échelle il faut que l’enfant ait déjà atteint un certain âge afin d’être apte de comprendre l’échelle qui lui est proposée et d’estimer correctement ce qu’il ressent.
    </p>
    <p>Dans cette catégorie d’échelle on retrouve : </p>
    <ul>
        <li>
            l’échelle de Wong-Baker <a href = "#Fig3">(Figure 3)</a> (et de manière plus générale les échelles de visages constituent les échelles les mieux validées et les plus efficaces auprès des enfants, figure) est destinée aux enfants de plus de 3 ans. 
            L’enfant dispose d’une palette comportant 5 dessins de visages plus ou moins déformés par la douleur. Il doit alors choisir  le visage qui correspond le mieux à ce qu’il éprouve.
        </li>
        <img src = "#" alt = "" id = "Fig3">
        <h5>Figure 3. Echelle Echelle de Wong-Baker</h5>
        <br>
        <li>l’échelle numérique (EN) qui vise à faire choisir un nombre de 0 à 10 quantifiant la douleur perçue par l’enfant. 0 représentant l’absence de douleur et 10 la plus forte douleur possible.</li>
        <li>
            l’échelle verbale simple (EVS) qui consiste à demander à l’enfant de qualifier sa douleur à l’aide d’adjectifs simples et choisis préalablement. 
            Le choix des mots utilisés dans l’échelle doit être adapté au stade de développement de l’enfant. 
            Bien qu’utilisée en pratique, cette échelle reste néanmoins peu précise et n’est toujours pas validée pour l’instant.
        </li>
    </ul>
