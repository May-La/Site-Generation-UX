    <h2 id = "$1">Le rapport à l’enfant et à ses capacités propres</h2>
    <br>
    <p>
        Lobjoie Nicolas nous apprend dans un article pour Altics (voir les sources) que, pour les jeunes enfants, l'évaluation de l’état émotionnel et du ressenti de l’enfant se fait souvent par un tiers en fonction des expressions faciales de l’enfant, de ses réactions, son comportement…
        Néanmoins au-delà de 6 ans il devient possible de laisser l’enfant s’auto-évaluer par le biais d’une échelle ou d’un dialogue.
        Cependant, il faut faire attention au design en adaptant une échelle pour des enfants, notamment sur le fait que la génération ciblée présente parfois des différences (culturelles, sociales, éducatives…) avec celle qui conçoit l’échelle. 
        L’échelle ne doit pas heurter ou exclure les enfants, ils doivent se sentir à l’aise en utilisant l’échelle afin d’obtenir de meilleurs résultats.
    </p>
    <p>
        Le public cible étant exclusivement composé d’enfants de 7 à 14 ans, il convient de respecter les facultés propres à ces derniers. <br>
        A ce stade de développement, le mode de pensée de l’enfant n’est plus seulement égocentrique, il est maintenant capable de réfléchir sur des événements ou des expériences qu’il n’a pas lui-même vécus. 
        A partir de ses nombreuses expériences vécues, l’enfant va pouvoir imaginer et se mettre à la place des autres, il commence à établir des liens de cause à effet et être davantage conscient des conséquences de certains événements grâce notamment à son propre vécu. 
        Face à une nouvelle expérience, l’enfant va se baser sur ces notions pour fonder sa réflexion et établir des schémas de pensée. Pour ce qui est de la classification et de la catégorisation, les enfants peuvent réaliser des regroupements plus avancées comme la sériation 
        (classer une série d’objets suivant une ou plusieurs caractéristiques : taille, poids, couleur) ou l’inférence transitive (établir la relation entre deux objets) ainsi que l’inclusion de classe (reconnaître les liens entre le tout et les parties).
    </p>
    <p>
        Finalement, lors du stade opératoire concret (cette étape du développement de l’enfant introduite par Piaget se situerait entre 7 et 11 ans environ, elle caractérise les premières réflexions logiques ou opérationnelles ce qui va permettre à l’enfant de comprendre et respecter les règles qui lui sont imposées), 
        le raisonnement transductif (permet au sujet d’associer des idées, lorsqu’il est face à une situation nouvelle, de réagir en fonction des acquis provenant d'événements passés qu’il juge similaires) des enfants évolue et ce dernier développe un raisonnement inductif (permettant de généraliser des idées à partir d'événements concrets) 
        qui lui permet, à partir d’observations de quelques exemples, de tirer des conclusions générales. 
        De plus, l’enfant commence à acquérir un raisonnement déductif (utilise l’abstraction, la recombinaison et les connaissances précédemment acquises pour en générer de nouvelles) et arrive à résoudre quelques problèmes. 
    </p>
    <p>
        Toujours d’après Lobjoie Nicolas, il y a quelques points importants à respecter lors du protocole de test chez l’enfant :
    </p>
    <ul>
        <li>Le test doit absolument durer moins d’une heure (et inclure des pauses si le test est long).</li>
        <li>L’accent doit être mis sur le jeu et l’aspect ludique du jeu afin de maintenir l’attention de l’enfant et créer de l’engagement chez lui.</li>
        <li>Il faut simplifier les tâches à accomplir au maximum, quitte à ce que l’enfant se fasse aider pour éviter tout blocage de leur côté.</li>
        <li>Le cadre environnant doit être rassurant, si les enfants ne sont pas à l’aise les tests peuvent être biaisés (pour cela la présence des parents peut représenter un atout, même si cela semble difficile à mettre en place en pratique).</li>
        <li>Les comportements verbaux et non verbaux révèlent beaucoup du ressenti de l’enfant.</li>
        <li>Établir une consigne et des questions claires et s’assurer que les enfants les comprennent.</li>
        <li>N’intervenir qu’en cas de besoin.</li>
    </ul>
    <p>Pour les enfants on peut évaluer (ou compléter l’évaluation) l'expérience qu’il ont eu avec un système avec d’autres outils qu’une échelle  :</p>
    <ul>
        <li>en se fiant au taux d’abandon (si le système offre cette possibilité).</li>
        <li>en mettant en place une “heatmap” qui nous renseignera sur les zones où l’enfant a le plus cliqué.</li>
        <li>en utilisant de l’eye tracking pour avoir accès aux endroits ayant le plus attiré le regard de l’enfant.</li>
        <li>en filmant/observant la session pour avoir accès aux réactions et expressions de l’enfant pendant qu’il utilise le système.</li>
    </ul>
