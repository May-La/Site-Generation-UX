    <h2>Echelles d’évaluation de l’anxiété</h2>
    <br>
    <p>
        Les processus de création d’une échelle de mesure de l’anxiété chez l’enfant sont détaillés dans la thèse Elaboration de l’échelle EVAN, 
        une échelle d’évaluation de l’anxiété avant les soins aux urgences pédiatriques chez les enfants de 3 à 16 ans de Marie Pourchet pour les enfants hospitalisés :
    </p>
    <p>
        Il existe des moyens de quantifier l’anxiété préopératoire chez l’enfant comme par exemple au travers du score “YALE” appelé le score YPAS, puis m-YPAS 
        (modified Yale Preoperative Anxiety Scale), ce score est obtenu grâce à une hétéroévaluation de l’anxiété pour des enfants âgés d’au moins 5 ans.
        <br>
        Pour des enfants un peu plus âgés, l’auto-évaluation devient possible, c’est ce qui est fait avec l’échelle EVA-anxiété convenant aux enfants entre 7 et 16 ans et qui permet d’évaluer l’anxiété péri-opératoire de manière rapide et répétée.
        <br>
        De même, l’échelle d’anxiété de Spielberger a été développée initialement pour des enfants de 9 à 12 ans, mais elle a largement été testée pour des enfants ayant entre 5 et 18 ans. 
        Elle permet de quantifier l’anxiété chez les enfants sans pour autant permettre d’en déterminer la cause.
    </p>
    <p>
    Sinon il existe des “échelles des émotions” pour essayer d’avoir une idée approchée de l’état émotionnel de l’enfant et/ou de son ressenti mais ces échelles ne sont pas fiables et ne sont pas validées.
    </p>
