    <h2 id = "CSUQ">Echelle CSUQ</h2>
    <br>
    <h3>Pourquoi?</h3>
    <br>
    <p>
        Des 3 échelles d’utilisabilité <a href = "Utilisabilite.php">vues précédemment</a>, l’échelle CSUQ est celle qui offre les résultats les plus complets. 
        En effet l’échelle permet d’avoir différents scores pour différentes parties du produit: un score sur l’utilité du système, un score sur la qualité de l'information, un score sur la qualité de l’interface, un score sur la satisfaction générale et un score global. 
        De plus, il est facilement possible de comparer ces scores dans le temps ( Lewis, 2002).
    </p>
    <br>
    <h3>Pistes d'adaptation</h3>
    <br>
    <ul>
        <li>Réduire le Likert à 7 items par un Likert à 5 items ou alors une échelle plus explicite comme recommandé par Borgers et Hox (2000) </li>
        <li>Revoir la formulation des affirmations pour éviter toute incompréhension. </li>
    </ul>
    <br>
    <hr ALIGN=CENTER WIDTH="50%"> 
    <br>
    <h2>Echelle PrEmo</h2>
    <br>
    <h3>Pourquoi?</h3>
    <br>
    <p>
        L’échelle PrEmo est intéressante à adapter puisque c’est une échelle assez intéractive, surtout en version numérique, et l'interactivité est une dimension importante pour l’attention de l’enfant. 
        De plus, le fait que les émotions soient communiquées sous forme de visage, les influences socio-culturelles ou éducatives sont atténuées, et enfin le test est plutôt court, ce qui est appréciable si on veut l’adapter pour des enfants.
    </p>
    <br>
    <h3>Pistes d'adaptation</h3>
    <br>
    <ul>
        <li>Simplifier les consignes au départ, les rendre courtes et concises </li>
        <li>Donner le nom de l’émotion associée à l’emoji  </li>
    </ul>
    <br>
    <hr ALIGN=CENTER WIDTH="50%"> 
    <br>
    <h2>Echelle MeCue</h2>
    <br>
    <h3>Pourquoi?</h3>
    <br>
    <p>
        Des 3 échelles UX présentées, cette échelle permet d’évaluer plus de critères différents et possède une évaluation globale. 
        De plus, l’échelle meCue est celle qui se démarque le plus des échelles d’utilisabilité, et, dans l’hypothèse où nous adapterions les trois types d’échelles présentés dans l’état de l’art, 
        le plus intéressant est de varier les critères évalués afin de récolter un plus grand nombre de données.
    </p>
    <br>
    <h3>Pistes d'adaptation</h3>
    <br>
    <ul>
        <li>Garder l'évaluation globale</li>
        <li>Simplifier le vocabulaire et la formulation des phrases pour faciliter la compréhension des items</li>
        <li>Réduire le nombre d’item général, l’idéal serait d’avoir entre 10 et 15 items</li>
        <li>Utiliser une aide visuelle pour faciliter le choix du cercle à cocher</li>
    </ul>
