    <h2 id = "titre">Validation de l’échelle</h2>
    <br>
    <p>Pour vérifier qu’une échelle est fonctionnelle, il faut s’assurer (Nunnally, 1978) par le biais de tests sur des utilisateurs que :</p>
    <ul>
        <li>l’échelle est fidèle, c’est-à-dire qu’elle fournit des résultats semblables lorsqu’elle est appliquée dans les mêmes conditions</li>
        <li>l’échelle est valide, c'est-à-dire qu’elle mesure la bonne donnée (anxiété, UX, utilisabilité…)</li>
    </ul>
    <br>
    <p>
        Vérifier la fidélité de l’échelle revient à évaluer sa cohérence en la testant sur un échantillon le plus large possible de personnes judicieusement choisies (il doit être représentatif de la population ciblée par l’échelle). 
        On peut également répéter plusieurs fois les tests dans les mêmes conditions plusieurs fois pour s’assurer que les scores obtenus coïncident. 
        Il existe aussi des outils statistiques qui permettent de s'assurer de la fiabilité d’une échelle comme par exemple le coefficient alpha de Cronbach. 
        Ce dernier fournit un indice allant de 0 à 1 et quantifiant la cohérence entre les questions d’un test, c’est-à-dire qu'elles servent toutes à mesurer la donnée souhaitée. 
        Ce coefficient augmente si les questions sont corrélées entre elles. Néanmoins il présente des failles puisqu’il a aussi tendance à augmenter lorsque le nombre de questions posées augmente. 
    </p>
    <p>
        Il n’y a pas de processus standardisé pour tester la validité d’une échelle. Pourtant il est indispensable de s’assurer que cette dernière mesure bien ce qu’elle est censée mesurer. 
        Pour cela, il est judicieux d’effectuer plusieurs tests et de modifier l’échelle après chaque test de façon à la rendre la plus précise possible, puis de repasser des tests et de répéter ce processus jusqu’à être satisfait des résultats obtenus lors des tests. 
        Il est aussi primordial de comparer les résultats obtenus à chaque itération du test de l’échelle avec des résultats obtenus à l’aide d’échelles qui ont déjà été validées afin de voir s’ils sont représentatifs.
    </p>
    <p>
        C’est ce que l’on peut voir dans le cadre de la thèse de Marie Pourchet (2017), pour élaborer une échelle de mesure de l’anxiété chez l’enfant. 
        L'échelle a été testée sur des enfants entre 3 et 16 ans hospitalisés choisis selon certains critères bien précis. 
        Par exemple, les enfants souffrant de trop grandes douleurs, ceux dont les parents ou eux-mêmes s’opposaient au passage du test, ceux sous emprise de stupéfiants ou encore ceux ayant un dysfonctionnement cognitif ont été écartés des tests afin d’obtenir des résultats les plus représentatifs possible. 
        De même, l’échelle testée (EVAN) a été comparée avec une échelle déjà validée (m-YPAS) pour observer si les scores obtenues concordent et donc si l’échelle est fiable. Des tests statistiques ont aussi été passés par un professionnel dans le cadre de cette étude et notamment le test de Wilcoxon.
    </p>
    <p>
        On retrouve également ce genre de protocole dans les travaux d’un groupe de chercheurs guidés par Oğuzhan Erdinç qui ont élaboré une traduction turque de l’échelle CSUQ. 
        Une première version de cette échelle a été confectionnée en suivant un protocole bien précis. Tout d’abord après avoir imaginé une première esquisse de traduction, 
        les scientifiques ont demandé à plusieurs personnes bilingues de vérifier si le sens des mots et des phrases étaient bien respecté et de s’assurer que l’échelle ne comporte pas d’ambiguïté. 
        Cette étape a mené à la modification de 14 items sur 19. Ensuite, l’échelle a été testé auprès de 10 étudiants observés par 2 superviseurs afin de déceler les expressions qui peuvent poser problème, et un statisticien étrangé au projet a aussi évalué la compréhensibilité des mots utilisés. 
        Après cela, un spécialiste du langage turque a vérifié l’échelle. Enfin, l’échelle T-CSUQ a été testé sur un échantillon de 120 personnes afin de s’assurer que les scores obtenues concordent avec ceux provenant de CSUQ.
    </p>
